-- Company document settings for polished invoice / quotation PDF templates.

create table if not exists public.document_company_settings (
  setting_id uuid primary key default gen_random_uuid(),
  setting_key text not null unique default 'nanofix_default',
  company_name text not null default 'NANOFIX',
  company_tagline text default 'No-Hacking Leak Repair & Waterproofing Solutions',
  uen text,
  gst_registration_no text,
  address_line_1 text,
  address_line_2 text,
  phone text default '+65 8038 7877',
  whatsapp text default '+65 8038 7877',
  email text,
  website text default 'https://www.nanofixsg.com',
  logo_storage_path text,
  invoice_prefix text default 'NFX-INV',
  quotation_prefix text default 'NFX-QUO',
  payment_instructions text default 'Please make payment using the payment link provided in Customer Portal or contact NANOFIX for bank transfer instructions.',
  warranty_footer text default 'Warranty coverage is subject to NANOFIX warranty terms and the approved scope of work.',
  terms_footer text default 'This document is system-generated by NANOFIX Service Operations. All prices are in SGD unless otherwise stated.',
  primary_color text default '#FF5F00',
  secondary_color text default '#1E293B',
  is_active boolean not null default true,
  updated_by uuid references public.profiles(profile_id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

