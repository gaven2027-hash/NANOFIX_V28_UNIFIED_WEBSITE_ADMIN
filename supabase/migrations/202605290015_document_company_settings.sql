create index if not exists document_company_settings_active_idx on public.document_company_settings(is_active, updated_at desc);

create or replace function public.document_company_settings_touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists document_company_settings_touch_updated_at on public.document_company_settings;
create trigger document_company_settings_touch_updated_at
before update on public.document_company_settings
for each row execute function public.document_company_settings_touch_updated_at();

alter table public.document_company_settings enable row level security;

drop policy if exists "internal roles can read document company settings" on public.document_company_settings;
create policy "internal roles can read document company settings"
on public.document_company_settings for select
using (public.current_profile_role() in ('super_admin','operations_admin','finance','support'));

drop policy if exists "service role can write document company settings" on public.document_company_settings;
create policy "service role can write document company settings"
on public.document_company_settings for all
using (auth.role() = 'service_role')
with check (auth.role() = 'service_role');
